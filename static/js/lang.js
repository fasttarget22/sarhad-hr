var currentLang = localStorage.getItem('hr_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('hr_lang', lang);
  applyLang();
}

function t(key) {
  return (LANGS[currentLang] && LANGS[currentLang][key]) || LANGS['en'][key] || key;
}

function applyLang() {
  var L = LANGS[currentLang];
  document.documentElement.setAttribute('dir', L.dir);
  document.documentElement.setAttribute('lang', currentLang);
  document.body.style.fontFamily = L.font;
  // Apply all data-t attributes
  document.querySelectorAll('[data-t]').forEach(function(el) {
    var key = el.getAttribute('data-t');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-tp]').forEach(function(el) {
    var key = el.getAttribute('data-tp');
    el.setAttribute('placeholder', t(key));
  });
  // Update lang switcher buttons
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}
